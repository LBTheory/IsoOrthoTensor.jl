#------------------------------------------------------------------------------#
#                          Orthogonality Tensor Tests                          #
#------------------------------------------------------------------------------#

@testset "Orthogonality Tensor Tests                        " begin
    @test (1) == K(2)
    @test (2) == 洧도((K(2), K(2)), (1, 3))
    @test (3) == 洧도((K(2), K(2), K(2)), (1, 3, 5))
    @test (4) == 洧도((K(2), K(2), K(2), K(2)), (1, 3, 5, 7))
    @test (5) == 洧도((K(2), K(2), K(2), K(2), K(2)), (1, 3, 5, 7, 9))
end


